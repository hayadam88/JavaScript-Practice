//Create a function that takes a number as an argument,
//increments the number by +1 and returns the result.

function addition(num) {
    return ++num
}

//Create a function that takes two numbers as 
//arguments and return their sum.

function addition(a, b) {
    return a + b;
}


//Write a function that takes an integer minutes 
//and converts it to seconds.

function convert(minutes) {
    return minutes * 60
}


//There is a single operator in JavaScript, capable of providing 
//the remainder of a division operation. Two numbers are passed as parameters. The 
//first parameter divided by the second parameter will have a remainder, possibly zero. Return that value.

function remainder(x, y) {
    return x % y
}